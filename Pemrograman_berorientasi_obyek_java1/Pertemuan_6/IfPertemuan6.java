public class IfPertemuan6 {
    public static void main(String[] args) {
        int angka = 7;

        // Pernyataan IF: Mengecek apakah angka lebih besar dari 5
        if (angka > 5) {
            System.out.println("Angka lebih besar dari 5.");
        }

        // Program akan melanjutkan eksekusi di sini setelah pernyataan IF
        System.out.println("Program berakhir.");
    }
}
